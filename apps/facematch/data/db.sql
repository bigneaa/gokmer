CREATE TABLE famille(
	id INT(5) PRIMARY KEY AUTO_INCREMENT NOT NULL,
    nom VARCHAR(20) NOT NULL,
    mdpass VARCHAR (15),
    logo VARCHAR(15) DEFAULT 'defaultlogo.jpg',
)ENGINE=INNODB DEFAULT CHARSET=latin1;

